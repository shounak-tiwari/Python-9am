string = "the boys"

# capitalize is use for change the first letter of first word into capital/upper case
# it return their capitalize form  
cap = string.capitalize()
print(f"{string} after using capitalize function :  {cap}")


# title is use for change the first letter of each word into capital/upper case
# it return their titled form  
tit = string.title()
print(f"{string} after using title function :  {tit}")